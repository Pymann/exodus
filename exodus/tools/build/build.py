import os
import subprocess
from collections import defaultdict
from pathlib import Path
from typing import List, Optional, Set
import argparse
import concurrent.futures
import sys
import importlib.util
from typing import Union

from exodus.models.project import Project, ProjectConfig
from exodus.tools.clean.clean import CleanTool
from exodus.core.logger import get_logger


class BuildTool:
    """Tool for building the project."""

    def __init__(self, args: argparse.Namespace) -> None:
        self.args = args
        self.logger = get_logger(__name__)
        self.errorlevel = 0
        self.config_file = Path(
            getattr(args, "config", "exodus.json") or "exodus.json"
        )
        self.config_mtime: Optional[float] = None
        self.compiler_mtime: Optional[float] = None
        self.linker_mtime: Optional[float] = None
        self.pre_compilation_hook_mtime: Optional[float] = None
        self.pre_linkage_hook_mtime: Optional[float] = None

    def _get_dependencies(self, obj_file: Path) -> List[Path]:
        """Reads the .d file generated by the compiler to find header dependencies."""
        dep_file = obj_file.with_suffix(".d")
        if not dep_file.exists():
            return []

        try:
            with open(dep_file, "r") as f:
                content = f.read().replace("\\\n", " ")
                # The first line is usually 'target: dep1 dep2 ...'
                parts = content.split(":", 1)
                if len(parts) < 2:
                    return []
                deps = parts[1].split()
                return [Path(d) for d in deps]
        except Exception as e:
            self.logger.warning(
                f"Warning: Failed to parse dependency file {dep_file}: {e}"
            )
            return []

    def _should_recompile(
        self,
        source_file: Path,
        obj_file: Path,
    ) -> bool:
        """Determines if a file needs recompilation based on timestamps and dependencies."""
        if not obj_file.exists():
            return True

        obj_mtime = obj_file.stat().st_mtime

        # 0. Recompile when project configuration changed after object generation.
        if self.config_mtime is not None and self.config_mtime > obj_mtime:
            return True

        # 1. Check source file timestamp
        if source_file.stat().st_mtime > obj_mtime:
            return True

        # 2. Check dependencies from .d file (works for C/C++ headers and any
        #    compiler that emits Makefile-format dependency files).
        for dep in self._get_dependencies(obj_file):
            if dep.exists() and dep.stat().st_mtime > obj_mtime:
                return True

        # 3. Recompile if the compiler binary itself is newer than the object.
        if self.compiler_mtime is not None and self.compiler_mtime > obj_mtime:
            return True

        # 4. Recompile if the pre-compilation hook changed after object generation.
        if (
            self.pre_compilation_hook_mtime is not None
            and self.pre_compilation_hook_mtime > obj_mtime
        ):
            return True

        return False

    def _expand_cache(self, s: Union[str, Path], config: ProjectConfig) -> str:
        """Replaces __exodus_cache with the cache directory path.
        If the EXODUS_CACHE environment variable is set, it is used as the path.
        Otherwise falls back to <source_root>/__exodus_cache."""
        s = str(s)
        if "__exodus_cache" in s:
            import os

            env_cache = os.environ.get("EXODUS_CACHE")
            if env_cache:
                abs_cache = str(Path(env_cache).resolve())
            else:
                abs_cache = str(
                    (config.source_root / "__exodus_cache").resolve()
                )
            s = s.replace("__exodus_cache", abs_cache)
        return s

    @staticmethod
    def _project_build_root(config: ProjectConfig) -> Path:
        """Returns project-scoped build root: <build_root>/<project_name>."""
        project_name = (config.name or "project").strip() or "project"
        safe_name = "".join(
            ch if ch.isalnum() or ch in ("-", "_", ".") else "_"
            for ch in project_name
        )
        return config.build_root / safe_name

    @classmethod
    def _object_file_for_source(
        cls, source_file: Path, config: ProjectConfig
    ) -> Path:
        """Returns the primary object path for one configured source file.

        For `output_type == "wasm"`, the intermediate is emitted as LLVM-IR
        text (`.ll`) so that emcc can consume it (it cannot link x86_64
        native object files into a WASM binary).
        """
        rel_path = source_file.relative_to(config.source_root.resolve())
        suffix = ".ll" if config.output_type == "wasm" else ".o"
        return cls._project_build_root(config) / rel_path.with_suffix(suffix)

    @staticmethod
    def _resolve_dependency_path(dep: Path, config: ProjectConfig) -> Path:
        """Normalizes a depfile entry to an absolute path."""
        if dep.is_absolute():
            return dep.resolve()
        return (config.source_root / dep).resolve()

    @staticmethod
    def _aiml_side_suffix(config: ProjectConfig) -> str:
        """File extension for AIML import side-files. Native = `.o`, WASM = `.ll`
        (emcc consumes LLVM IR text; native linkers consume objects). Keeping
        this target-aware so other projects (9rooms, …) stay untouched."""
        return "_aiml.ll" if config.output_type == "wasm" else "_aiml.o"

    def _expected_generated_aiml_objects_for_source(
        self,
        source_file: Path,
        obj_file: Path,
        config: ProjectConfig,
    ) -> Set[Path]:
        """Derives AIML import objects expected as side products of one top-level AIML compile."""
        if source_file.suffix != ".aiml":
            return set()

        expected: Set[Path] = set()
        source_abs = source_file.resolve()
        source_root_abs = config.source_root.resolve()
        suffix = self._aiml_side_suffix(config)
        for dep in self._get_dependencies(obj_file):
            dep_abs = self._resolve_dependency_path(dep, config)
            if dep_abs == source_abs or dep_abs.suffix != ".aiml":
                continue
            try:
                rel_dep = dep_abs.relative_to(source_root_abs)
            except ValueError:
                continue
            obj_name = "_".join(rel_dep.with_suffix("").parts) + suffix
            expected.add(obj_file.parent / obj_name)
        return expected

    def _filter_discovered_extra_objects(
        self,
        source_files: List[Path],
        extra_objs: List[Path],
        config: ProjectConfig,
    ) -> List[Path]:
        """Keeps only extra objects that can be justified from current compiler sources/depfiles."""
        expected_aiml_objs: Set[Path] = set()
        suffix = self._aiml_side_suffix(config)
        allowed_explicit_objects: Set[Path] = {
            src.resolve() for src in source_files if src.suffix == ".o"
        }
        for source_file in source_files:
            if source_file.suffix == ".o":
                continue
            obj_file = self._object_file_for_source(source_file, config)
            expected_aiml_objs.update(
                self._expected_generated_aiml_objects_for_source(
                    source_file,
                    obj_file,
                    config,
                )
            )

        unexpected_aiml_objs = sorted(
            p
            for p in extra_objs
            if p.name.endswith(suffix) and p not in expected_aiml_objs
        )
        if unexpected_aiml_objs:
            self.logger.warning(
                "excluding %d unexpected AIML object(s) not referenced by"
                " depfiles: %s",
                len(unexpected_aiml_objs),
                ", ".join(p.name for p in unexpected_aiml_objs),
            )

        missing_expected_aiml_objs = sorted(
            p for p in expected_aiml_objs if not p.exists()
        )
        if missing_expected_aiml_objs:
            raise RuntimeError(
                "missing expected AIML object(s) after compile: "
                + ", ".join(p.name for p in missing_expected_aiml_objs)
            )

        unexpected_non_aiml_objs = sorted(
            p
            for p in extra_objs
            if not p.name.endswith(suffix)
            and p.resolve() not in allowed_explicit_objects
        )
        if unexpected_non_aiml_objs:
            self.logger.warning(
                "excluding %d unexpected extra object(s) not derived from"
                " configured compiler sources: %s",
                len(unexpected_non_aiml_objs),
                ", ".join(p.name for p in unexpected_non_aiml_objs),
            )

        rejected = set(unexpected_aiml_objs + unexpected_non_aiml_objs)
        return [p for p in extra_objs if p not in rejected]

    @staticmethod
    def _linked_output_name(config: ProjectConfig) -> str:
        output_name = config.name
        if config.output_type == "wasm":
            # emcc emits {name}.html + {name}.js + {name}.wasm
            output_name = f"{config.name}.html"
        elif os.name == "nt":
            output_name += ".exe"
        elif config.output_type == "static_lib":
            output_name = f"lib{config.name}.a"
        elif config.output_type == "shared_lib":
            prefix = "" if config.name.startswith("lib") else "lib"
            output_name = f"{prefix}{config.name}.so"
        return output_name

    @classmethod
    def _linked_output_file(cls, config: ProjectConfig) -> Path:
        output_root = (
            Path.cwd()
            if config.artifact_in_cwd
            else cls._project_build_root(config)
        )
        return output_root / cls._linked_output_name(config)

    @staticmethod
    def _duplicate_project_names(
        config_names: List[str],
    ) -> dict[str, List[str]]:
        by_name: dict[str, List[str]] = defaultdict(list)
        for config_name in config_names:
            project = Project.load(Path.cwd(), config_name=config_name)
            by_name[project.config.name].append(config_name)
        return {
            project_name: names
            for project_name, names in by_name.items()
            if len(names) > 1
        }

    def _compile_file(
        self, source_file: Path, config: ProjectConfig
    ) -> List[Path]:
        """Compiles a single source file. Returns the produced object file."""
        if source_file.suffix == ".o":
            if not source_file.exists():
                self.errorlevel = 1
                return []
            return [source_file.resolve()]

        if source_file.suffix in [".cpp", ".cc", ".cxx", ".c"]:
            compiler_name = self._expand_cache(config.compiler.name, config)
            base_flags = config.compiler.flags
            std_flag = ""

            if config.compiler.lang_standard:
                if source_file.suffix in [".cpp", ".cc", ".cxx"]:
                    std_flag = f"-std=c++{config.compiler.lang_standard}"
                elif source_file.suffix == ".c":
                    std_flag = f"-std=c{config.compiler.lang_standard}"
        else:
            compiler_name = self._expand_cache(config.compiler.name, config)
            base_flags = config.compiler.flags
            std_flag = ""

        # Build output path (mirroring source structure in build dir)
        rel_path = source_file.relative_to(config.source_root.resolve())
        obj_file = self._object_file_for_source(source_file, config)
        obj_file.parent.mkdir(parents=True, exist_ok=True)

        if not self._should_recompile(source_file, obj_file):
            self.logger.info(f"{rel_path} is up to date, skipping compilation")
            return [obj_file]

        # Construct command
        self.logger.info(f"compiling {rel_path}")

        is_standard_cc = source_file.suffix in (".c", ".cpp", ".cc", ".cxx")
        if is_standard_cc:
            # Standard GCC/Clang flags
            cmd = (
                [compiler_name]
                + base_flags
                + ["-c", str(source_file), "-o", str(obj_file), "-MMD", "-MP"]
            )

            if std_flag:
                cmd.append(std_flag)

            # Optimization
            cmd.append(f"-O{config.optimization}")

            # Debug
            if config.debug:
                cmd.append("-g")

            # Warnings
            for w in config.warnings:
                cmd.append(f"-W{w}")
            if config.werror:
                cmd.append("-Werror")
        else:
            # Custom compiler — pass source and output only
            cmd = [
                compiler_name,
                str(source_file.resolve()),
                "-o",
                str(obj_file.resolve()),
            ]
            # For wasm output, the AIML compiler must (a) target wasm32 so
            # pointer/size_t widths match emcc's expectations and (b) include
            # a main() entry point in the .ll so emcc can link a runnable.
            if config.output_type == "wasm" and source_file.suffix == ".aiml":
                cmd.extend(["--target", "wasm32", "--emit-main"])
            # Asset Registry (spec/44_7): forward asset_directories to the
            # AIML compiler so its AssetScanner can validate gl.tex(stem)
            # and prefab texture: fields at compile-time.
            if source_file.suffix == ".aiml" and config.asset_directories:
                cmd.append(
                    "--asset-roots="
                    + ",".join(str(p) for p in config.asset_directories)
                )

        # Includes
        for inc in config.search_paths:
            cmd.append(f"-I{self._expand_cache(inc, config)}")

        # Defines
        for name, value in config.defines.items():
            if value:
                cmd.append(f"-D{name}={value}")
            else:
                cmd.append(f"-D{name}")

        self.logger.debug(" ".join(cmd))

        if config.env:
            expanded_env = {
                k: self._expand_cache(v, config) for k, v in config.env.items()
            }
            proc_env = {**os.environ, **expanded_env}
        else:
            proc_env = None
        result = subprocess.run(
            cmd, check=False, env=proc_env
        )  # We allow failure to return []
        if result.returncode != 0:
            self.errorlevel = 1
            return []

        return [obj_file]

    def _external_link_inputs(self, config: ProjectConfig) -> List[Path]:
        """Returns paths to external library files (.so/.a) that should trigger a relink."""
        inputs: List[Path] = []
        lib_dirs = [
            Path(self._expand_cache(lp, config))
            for lp in config.linker.library_paths
        ]
        for lib in config.linker.libraries:
            for lib_dir in lib_dirs:
                for candidate in (f"lib{lib}.so", f"lib{lib}.a"):
                    p = lib_dir / candidate
                    if p.exists():
                        inputs.append(p)
                        break
        return inputs

    def _asset_input_files(self, config: ProjectConfig) -> List[Path]:
        """Files inside `asset_directories` that get bundled into the wasm output
        via emcc --preload-file. Their mtimes must invalidate the link cache —
        otherwise editing a shader/texture/font silently keeps the old bundle.
        """
        if config.output_type != "wasm" or not config.asset_directories:
            return []
        files: List[Path] = []
        for asset_dir in config.asset_directories:
            root = Path(self._expand_cache(asset_dir, config))
            if not root.exists():
                continue
            if root.is_file():
                files.append(root)
                continue
            for p in root.rglob("*"):
                if p.is_file():
                    files.append(p)
        return files

    def _link(self, obj_files: List[Path], config: ProjectConfig) -> None:
        """Links the object files into the final artifact."""
        if not obj_files:
            return

        # Determine linker (expand __exodus_cache → cache root path)
        raw_linker = (
            config.linker.name if config.linker.name else config.compiler.name
        )
        linker = self._expand_cache(raw_linker, config)

        output_name = self._linked_output_name(config)
        if config.output_type == "static_lib":
            # Default to host ar; allow explicit linker.name override (e.g. emar for wasm objects)
            raw = config.linker.name if config.linker.name else "ar"
            linker = self._expand_cache(raw, config)
        elif config.output_type == "wasm":
            # emcc handles linking AND emits .js / .wasm / .html
            raw = config.linker.name if config.linker.name else "emcc"
            linker = self._expand_cache(raw, config)

        output_file = self._linked_output_file(config)
        output_file.parent.mkdir(parents=True, exist_ok=True)

        # Relink if output missing or any .o is newer
        needs_relink = not output_file.exists()
        if not needs_relink:
            output_mtime = output_file.stat().st_mtime
            for obj in obj_files:
                if obj.stat().st_mtime > output_mtime:
                    needs_relink = True
                    break

        # Also relink if the linker binary itself is newer than output
        if not needs_relink and self.linker_mtime is not None:
            if self.linker_mtime > output_mtime:
                self.logger.debug("relink triggered by linker binary")
                needs_relink = True

        # Also relink if the project config itself is newer than output.
        if not needs_relink and self.config_mtime is not None:
            if self.config_mtime > output_mtime:
                self.logger.debug("relink triggered by project config")
                needs_relink = True

        # Also relink if hooks that can affect compile or link behavior changed.
        if not needs_relink and self.pre_compilation_hook_mtime is not None:
            if self.pre_compilation_hook_mtime > output_mtime:
                self.logger.debug("relink triggered by pre-compilation hook")
                needs_relink = True

        if not needs_relink and self.pre_linkage_hook_mtime is not None:
            if self.pre_linkage_hook_mtime > output_mtime:
                self.logger.debug("relink triggered by pre-linkage hook")
                needs_relink = True

        # Also relink if any external library (.so/.a) is newer than output
        if not needs_relink:
            output_mtime = output_file.stat().st_mtime
            for ext_lib in self._external_link_inputs(config):
                if ext_lib.stat().st_mtime > output_mtime:
                    self.logger.debug("relink triggered by %s", ext_lib.name)
                    needs_relink = True
                    break

        # Also relink if any preloaded asset (wasm only) is newer than output.
        # Without this, editing e.g. a shader file silently keeps the old bundle
        # because exodus thinks "no .o changed → up to date".
        if not needs_relink:
            output_mtime = output_file.stat().st_mtime
            for asset in self._asset_input_files(config):
                if asset.stat().st_mtime > output_mtime:
                    self.logger.debug("relink triggered by asset %s", asset)
                    needs_relink = True
                    break

        if not needs_relink:
            self.logger.info(f"{output_name} is up to date, skipping linkage")
            return

        if config.output_type == "static_lib":
            self.logger.info(f"linking {output_name}")
            cmd = [linker, "rcs", str(output_file)] + [
                str(o) for o in obj_files
            ]
        else:
            self.logger.info(f"linking {output_name}")
            cmd = (
                [linker]
                + [str(o) for o in obj_files]
                + ["-o", str(output_file)]
            )

            if config.output_type == "shared_lib":
                cmd.append("-shared")

            # Asset directories → emcc --preload-file (wasm only)
            if config.output_type == "wasm":
                for asset_dir in config.asset_directories:
                    expanded = self._expand_cache(asset_dir, config)
                    cmd.append(f"--preload-file={expanded}")

            # Linker Flags
            cmd.extend(
                self._expand_cache(f, config) for f in config.linker.flags
            )

            # Linker Script
            if config.linker.script:
                cmd.append(f"-T{config.linker.script}")

            # Map File
            if config.linker.map_file:
                cmd.append(f"-Wl,-Map={config.linker.map_file}")

            # Library Paths
            for lib_path in config.linker.library_paths:
                expanded = self._expand_cache(lib_path, config)
                cmd.append(f"-L{expanded}")
                if config.linker.rpath and ":" not in expanded:
                    cmd.append(f"-Wl,-rpath,{expanded}")

            # Libraries
            for lib in config.linker.libraries:
                cmd.append(f"-l{lib}")

        self.logger.debug(" ".join(cmd))

        if config.env:
            expanded_env = {
                k: self._expand_cache(v, config) for k, v in config.env.items()
            }
            proc_env = {**os.environ, **expanded_env}
        else:
            proc_env = None
        result = subprocess.run(cmd, check=True, env=proc_env)
        if self.errorlevel == 0 and result.returncode != 0:
            self.errorlevel = 1

    def _execute_hook(self, script_path: Path, config: ProjectConfig) -> None:
        """Executes a python hook script that can modify the configuration."""
        if not script_path.exists():
            return

        try:
            # Load the module dynamically
            spec = importlib.util.spec_from_file_location(
                "hook_module", script_path
            )
            if spec and spec.loader:
                module = importlib.util.module_from_spec(spec)
                sys.modules["hook_module"] = module
                spec.loader.exec_module(module)

                # Check for run function
                if hasattr(module, "run"):
                    module.run(config)
                else:
                    pass
            else:
                pass

        except Exception as e:
            raise

    def _toposort_configs(
        self, config_names: List[str]
    ) -> Optional[List[str]]:
        """Returns config_names sorted so dependencies build before dependents.
        Returns None if a dependency cycle is detected."""
        projects = {
            cn: Project.load(Path.cwd(), config_name=cn) for cn in config_names
        }
        name_to_config = {p.config.name: cn for cn, p in projects.items()}

        # graph[cn] = list of config_names cn depends on (must be built first)
        graph: dict[str, List[str]] = {cn: [] for cn in config_names}
        for cn, proj in projects.items():
            for dep in proj.config.dependencies:
                dep_config = name_to_config.get(dep.name)
                if dep_config and dep_config != cn:
                    graph[cn].append(dep_config)

        # Kahn's algorithm: reverse edges (dep → dependent) for in-degree tracking
        in_degree: dict[str, int] = {cn: 0 for cn in config_names}
        reverse: dict[str, List[str]] = {cn: [] for cn in config_names}
        for cn, deps in graph.items():
            for dep_config in deps:
                in_degree[cn] += 1
                reverse[dep_config].append(cn)

        queue = [cn for cn in config_names if in_degree[cn] == 0]
        result: List[str] = []
        while queue:
            node = queue.pop(0)
            result.append(node)
            for dependent in reverse[node]:
                in_degree[dependent] -= 1
                if in_degree[dependent] == 0:
                    queue.append(dependent)

        if len(result) != len(config_names):
            return None  # cycle detected
        return result

    def run(self) -> int:
        config_name = (
            getattr(self.args, "config", "exodus.json") or "exodus.json"
        )
        if getattr(self.args, "all", False):
            config_names = Project.discover_config_names(Path.cwd())
            if not config_names:
                self.logger.error(
                    "No Exodus project config JSON files found in %s.",
                    Path.cwd(),
                )
                return 1
            duplicate_names = self._duplicate_project_names(config_names)
            if duplicate_names:
                for project_name, names in sorted(duplicate_names.items()):
                    joined = ", ".join(sorted(names))
                    self.logger.error(
                        "Build --all requires unique project names. "
                        "Project '%s' is defined by: %s",
                        project_name,
                        joined,
                    )
                return 1

            sorted_configs = self._toposort_configs(config_names)
            if sorted_configs is None:
                self.logger.error(
                    "Build --all aborted: dependency cycle detected among"
                    " project configs."
                )
                return 1

            failures = 0
            for candidate in sorted_configs:
                self.logger.info("building project config %s", candidate)
                rc = self._run_config(candidate)
                if rc != 0:
                    failures += 1
            if failures:
                self.logger.error(
                    "Build finished with %d failing config(s).", failures
                )
                return 1
            return 0

        config_path = Path.cwd() / config_name
        if not config_path.exists():
            self.logger.error("Config file not found: %s", config_path)
            return 1
        return self._run_config(config_name)

    def _run_config(self, config_name: str) -> int:
        # Load Project
        # TODO: Ideally pass the project path via args or find exodus.toml
        self.errorlevel = 0
        project = Project.load(Path.cwd(), config_name=config_name)
        self.config_file = Path(config_name)
        if self.config_file.exists():
            self.config_mtime = self.config_file.stat().st_mtime
        else:
            self.config_mtime = None

        compiler_path = Path(project.config.compiler.name)
        if compiler_path.is_file():
            self.compiler_mtime = compiler_path.stat().st_mtime
        else:
            self.compiler_mtime = None

        linker_name = (
            project.config.linker.name or project.config.compiler.name
        )
        linker_path = Path(linker_name)
        if linker_path.is_file():
            self.linker_mtime = linker_path.stat().st_mtime
        else:
            self.linker_mtime = None

        pre_compilation_hook = project.config.pre_compilation
        if pre_compilation_hook and pre_compilation_hook.exists():
            self.pre_compilation_hook_mtime = (
                pre_compilation_hook.stat().st_mtime
            )
        else:
            self.pre_compilation_hook_mtime = None

        pre_linkage_hook = project.config.pre_linkage
        if pre_linkage_hook and pre_linkage_hook.exists():
            self.pre_linkage_hook_mtime = pre_linkage_hook.stat().st_mtime
        else:
            self.pre_linkage_hook_mtime = None

        if self.args.clean:
            import copy as _copy

            clean_args = _copy.copy(self.args)
            clean_args.all = False
            CleanTool(clean_args).run(project)

        # Pre-compilation Hook
        if project.config.pre_compilation:
            self._execute_hook(project.config.pre_compilation, project.config)

        # Collect Sources
        # Assuming project.config.sources contains glob patterns, defaults to all C/C++ in source_root if empty
        source_files: List[Path] = []
        if not project.config.sources:
            # Default behavior: find all .c, .cpp, .cc
            for ext in ["**/*.c", "**/*.cpp", "**/*.cc"]:
                source_files.extend(project.config.source_root.glob(ext))
        else:
            for pattern in project.config.sources:
                source_files.extend(project.config.source_root.glob(pattern))

        source_files = sorted({p.resolve() for p in source_files})

        # Compile
        obj_files: List[Path] = []
        max_workers = self.args.jobs if self.args.jobs > 0 else None

        # Parallel compilation
        with concurrent.futures.ThreadPoolExecutor(
            max_workers=max_workers
        ) as executor:
            futures = {
                executor.submit(self._compile_file, src, project.config): src
                for src in source_files
            }

            for future in concurrent.futures.as_completed(futures):
                src = futures[future]
                try:
                    objs = future.result()
                    if objs:
                        obj_files.extend(objs)
                    else:
                        self.logger.error(f"Build failed for {src}.")
                        return 1
                except Exception as exc:
                    self.logger.error(
                        f"Compilation generated an exception for {src}: {exc}"
                    )
                    return 1

        # Collect AIML import side-files in build dir. Native: `.o`; WASM:
        # `.ll` (text IR, consumed by emcc directly).
        build_root = self._project_build_root(project.config)
        known_objs = set(obj_files)
        scan_glob = "*.ll" if project.config.output_type == "wasm" else "*.o"
        extra_objs = [
            p for p in build_root.rglob(scan_glob) if p not in known_objs
        ]
        extra_objs = self._filter_discovered_extra_objects(
            source_files,
            extra_objs,
            project.config,
        )
        if extra_objs:
            self.logger.info(
                "found %d additional object(s) via directory scan: %s",
                len(extra_objs),
                ", ".join(p.name for p in extra_objs),
            )
        for extra_o in extra_objs:
            obj_files.append(extra_o)
            known_objs.add(extra_o)

        # Pre-linkage Hook
        if project.config.pre_linkage:
            self._execute_hook(project.config.pre_linkage, project.config)

        # Link
        self._link(obj_files, project.config)
        return self.errorlevel
